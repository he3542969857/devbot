"""评测执行器 —— 跑 evaluator 拿预测、算每个 metric、落 trace(sqlite,尽力而为)。

run():  一次评测,得到 {metric_name: MetricResult-as-dict}。
compare(): A/B 两个 evaluator 跑同一批样本同一组 metric,用于消融(如 dedup on/off)。

设计要点:
- evaluator / metric / judge 都是协议,这里只按协议调用,不假定具体实现。
- 若某 metric 是 LlmJudgeMetric 且传了 judge,则把 judge 注入该 metric(支持
  set_judge() 方法或 .judge 属性两种接法),让它用真/确定性裁判打分。
- trace 落到 .devbot-eval/trace.sqlite 的 runs(id,suite,metric,value,created_at),
  纯 stdlib sqlite3,任何异常都吞掉(评测结果本身不依赖 trace 成功)。
"""
from __future__ import annotations

import os
import time
from dataclasses import asdict, is_dataclass
from typing import Any, Optional

from devbot_eval.domain import PRReviewOutput
from devbot_eval.sample import EvalSample

# trace 落 Postgres(devplatform 库 eval_runs 表),尽力而为;不再用 SQLite。
_TRACE_PG_DSN = os.environ.get(
    "DEVPLATFORM_PG_DSN",
    "host=127.0.0.1 port=5432 dbname=devplatform user=devplatform password=CHANGE_ME_PG_PASSWORD",
)


# ── trace 持久化(尽力而为)────────────────────────────────────────────
def _persist_trace(suite: str, results: dict[str, "MetricResultLike"]) -> None:
    """把本次每个 metric 的 value 写入 PG 的 eval_runs 表;整体 try/except 包住、失败不阻断评测。"""
    try:
        import psycopg
        created = time.strftime("%Y-%m-%dT%H:%M:%S")
        rows = []
        for metric_name, mr in results.items():
            value = mr.get("value") if isinstance(mr, dict) else getattr(mr, "value", None)
            try:
                value = float(value)
            except (TypeError, ValueError):
                value = 0.0
            rows.append((suite, metric_name, value, created))
        with psycopg.connect(_TRACE_PG_DSN, autocommit=True) as conn:
            cur = conn.cursor()
            cur.execute(
                "CREATE TABLE IF NOT EXISTS eval_runs ("
                "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, "
                "suite TEXT, metric TEXT, value DOUBLE PRECISION, created_at TEXT)")
            if rows:
                cur.executemany(
                    "INSERT INTO eval_runs (suite, metric, value, created_at) VALUES (%s,%s,%s,%s)", rows)
    except Exception:
        pass


# ── 内部工具 ────────────────────────────────────────────────────────────
def _is_llm_judge_metric(metric: Any) -> bool:
    """是否 LlmJudgeMetric(惰性导入,导入失败则退化为按类名判断)。"""
    try:
        from devbot_eval.metrics.llm_judge import LlmJudgeMetric
        if isinstance(metric, LlmJudgeMetric):
            return True
    except Exception:
        pass
    return type(metric).__name__ == "LlmJudgeMetric"


def _wire_judge(metric: Any, judge: Any) -> None:
    """把 judge 接进 LlmJudgeMetric:优先 set_judge(),否则直接挂 .judge 属性。"""
    if judge is None:
        return
    setter = getattr(metric, "set_judge", None)
    if callable(setter):
        setter(judge)
    else:
        try:
            metric.judge = judge
        except Exception:
            pass


def _result_to_dict(mr: Any) -> dict[str, Any]:
    """MetricResult(dataclass)→dict;已是 dict 原样返回;其它尽力提取字段。"""
    if isinstance(mr, dict):
        return mr
    if is_dataclass(mr) and not isinstance(mr, type):
        return asdict(mr)
    return {
        "name": getattr(mr, "name", ""),
        "value": getattr(mr, "value", 0.0),
        "breakdown": getattr(mr, "breakdown", {}),
        "extras": getattr(mr, "extras", {}),
    }


def _suite_label(samples: list[EvalSample]) -> str:
    """从样本推断 suite 标签;多 suite 混跑则标 'all'。"""
    suites = {s.suite for s in samples if getattr(s, "suite", None)}
    if not suites:
        return "unknown"
    if len(suites) == 1:
        return next(iter(suites))
    return "all"


def _predict_all(samples: list[EvalSample], evaluator: Any) -> list[PRReviewOutput]:
    """对每个样本调 evaluator.evaluate;单样本失败不拖垮整批,产一个空输出占位。"""
    preds: list[PRReviewOutput] = []
    for s in samples:
        try:
            preds.append(evaluator.evaluate(s))
        except Exception as e:
            from devbot_eval.domain import RiskLevel
            preds.append(PRReviewOutput(
                pr_id=getattr(s, "pr_id", ""), risk_score=0,
                risk_level=RiskLevel.LOW, summary=f"[evaluator-error] {e}",
            ))
    return preds


# ── 公开入口 ────────────────────────────────────────────────────────────
def run(samples: list[EvalSample], evaluator: Any, metrics: list[Any],
        judge: Any = None) -> dict[str, Any]:
    """跑一次完整评测。

    步骤:① 用 evaluator 对每个样本产 PRReviewOutput;② 若某 metric 是
    LlmJudgeMetric 且给了 judge 则把 judge 接进去;③ 逐 metric 计算;
    ④ 尽力把每个 metric 的 value 落到 trace.sqlite。

    返回 {metric_name: MetricResult-as-dict}。某个 metric 抛错时记一个
    value=0 的错误结果,不影响其它 metric。
    """
    predictions = _predict_all(samples, evaluator)
    suite = _suite_label(samples)

    out: dict[str, Any] = {}
    for metric in metrics:
        if judge is not None and _is_llm_judge_metric(metric):
            _wire_judge(metric, judge)
        name = getattr(metric, "name", type(metric).__name__)
        try:
            mr = metric.evaluate(samples, predictions)
            out[name] = _result_to_dict(mr)
        except Exception as e:
            out[name] = {"name": name, "value": 0.0, "breakdown": {},
                         "extras": {"error": str(e)}}

    _persist_trace(suite, out)
    return out


def compare(samples: list[EvalSample], evaluatorA: Any, evaluatorB: Any,
            metrics: list[Any]) -> dict[str, Any]:
    """A/B 消融:同一批样本、同一组 metric,跑两个 evaluator 对比。

    典型用途:dedup on(A)vs dedup off(B),或真 Agent vs mock。
    返回 {'A': {...}, 'B': {...}, 'delta': {metric: B_value - A_value}}。
    注意:为避免 judge 状态串台,这里不接 judge;如需 judge 评测请分别调 run()。
    """
    res_a = run(samples, evaluatorA, list(metrics), judge=None)
    res_b = run(samples, evaluatorB, list(metrics), judge=None)

    delta: dict[str, float] = {}
    for name in res_a:
        va = res_a.get(name, {}).get("value", 0.0) or 0.0
        vb = res_b.get(name, {}).get("value", 0.0) or 0.0
        try:
            delta[name] = round(float(vb) - float(va), 6)
        except (TypeError, ValueError):
            delta[name] = 0.0

    return {"A": res_a, "B": res_b, "delta": delta}


# 仅供类型注释可读性,不强约束运行时类型
MetricResultLike = Any
